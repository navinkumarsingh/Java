package ex4_ConditionalStatements;

// Class to check if a number is positive or negative
public class Ex1_CheckPositiveOrNegative {

    public static void main(String[] a) {
        // Declare and initialize an integer variable with a value of 5
        int b = 5;

        // Check if the number is positive or negative
        if (b >= 0) {
            // Print if the number is positive (0 is considered positive here)
            System.out.println("Positive Number");
        } else {
            // Print if the number is negative
            System.out.println("Negative Number");
        }
    }
}
