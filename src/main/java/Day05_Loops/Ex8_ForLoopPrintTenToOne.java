// Define the package this file belongs to
package Day05_Loops;

// Main class to demonstrate printing numbers from 10 to 1 using a for loop
public class Ex8_ForLoopPrintTenToOne {
    // Main method - program starts executing here
    public static void main(String[] args) {

        // For loop starts with i=10, runs as long as i is greater than 0
        // After each iteration, i is decreased by 1 (i--)
        for (int i = 10; i > 0; i--) {

            // Print the current value of i on the console
            System.out.println(i);
        }
    }
}
