// Define the package this file belongs to
package Day05_Loops;

// Main class to demonstrate printing numbers from 1 to 10 using a for loop
public class Ex7_ForLoopPrintOneToTen {
    // Main method - program starts executing here
    public static void main(String[] args) {

        // For loop starts with i=1, runs as long as i is less than or equal to 10
        // After each iteration, i is incremented by 1
        for (int i = 1; i <= 10; i++) {

            // Print the current value of i on the console
            System.out.println(i);
        }
    }
}
