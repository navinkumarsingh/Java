package ex5_Loops;

// Main class to demonstrate an infinite while loop
public class Ex4_InfiniteWhileLoopOrWihoutCondition {

    // Main method - program entry point
    public static void main(String[] args) {

        // Start a while loop with condition 'true' which means it will run forever
        while (true) {
            // Print this message continuously because the loop never ends
            System.out.println("This is an infinite loop");
        }
    }
}
