package ex5_Loops; // Package name (used to organize classes, not mandatory for basic programs)

public class Ex16_Print1To10InDescOrder { // Class definition
    public static void main(String[] args) { // Main method - program execution starts here

        // 'for' loop starts from i = 10 and decreases until i > 0
        for(int i = 10; i > 0; i--) {
            System.out.println(i); // Prints the current value of i
        }
    }
}
